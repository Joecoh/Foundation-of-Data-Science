import numpy as np

# Original array
original_array = np.array([1, 2, 3])

# Values to append
values_to_append = np.array([4, 5, 6])

# Appending the values to the original array
result_array = np.append(original_array, values_to_append)

# Display the original array and the result array
print("Original Array:")
print(original_array)

print("\nArray after Appending Values:")
print(result_array)
